import { getSum } from "../../js/main";

describe("getSum", () => {
  test("правильно додає два числа", () => {
    // Перевіряємо, що результат виклику функції getSum з аргументами 1 і 2 дорівнює 3
    expect(getSum(1, 2)).toEqual(3);
  });

  test("правильно додає від'ємні числа", () => {
    // Перевіряємо, що результат виклику функції getSum з аргументами -1 і -2 дорівнює -3
    expect(getSum(-1, -2)).toEqual(-3);
  });
});
